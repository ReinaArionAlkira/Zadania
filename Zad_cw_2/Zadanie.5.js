const readlineSync = require('readline-sync');


var suma = 0;
var licznik = 0;
var srednia = 0;
var min = Number.MAX_VALUE;
var max = Number.MIN_VALUE;
do {
    var a = readlineSync.questionInt();
    suma += a;
    if (a == 0) break;
    if (a < min) min = a;
    if (a > max) max = a;
    licznik++;
}
while (a != 0) {
    srednia= suma/licznik;
    console.log("Suma liczb:"+ suma)
    console.log("Åšrednia liczb:"+ srednia)
    console.log("Minimalna:"+ min)
    console.log("Maksymalna:"+ max)
}
