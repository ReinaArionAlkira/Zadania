const readlineSync = require('readline-sync');

var liczba = readlineSync.questionInt();
var dzielnik = 2;
if (liczba <= 1)
  liczba = readlineSync.questionInt();


for (var i=0; i<liczba; i++) {
  if(liczba%dzielnik==0) {
    console.log("nie")
    break;
  }
  if (liczba%dzielnik!=0) {
    console.log("tak")
    break;
  }
  dzielnik++;
}
